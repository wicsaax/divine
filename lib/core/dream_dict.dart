/// 在文本里扫关键词, 返回命中的条目 (顺序按字典顺序).
List<DreamEntry> matchEntries(String text) {
  final matched = <DreamEntry>[];
  final lower = text.toLowerCase();
  for (final entry in zhouGongDict) {
    for (final kw in entry.keywords) {
      if (lower.contains(kw.toLowerCase())) {
        matched.add(entry);
        break;
      }
    }
  }
  return matched;
}
